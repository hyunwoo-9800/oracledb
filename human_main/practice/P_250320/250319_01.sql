-- LG상품 중 80만원 초과 / 상품코드 순 출력
SELECT
    PRODUCT_CODE,
    PRODUCT_NAME,
    UNIT,
    UNIT_PRICE,
    LEFT_QTY,
    COMPANY
FROM
    EC_PRODUCT
WHERE
    COMPANY LIKE 'LG%'
    AND UNIT_PRICE > 800000
ORDER BY
    PRODUCT_CODE;

-- 생산처가 없는 제품 / 상품코드 순 출력
