-- CM01 1개 초과 주문 / 주문자 ID 순 출력
SELECT
    ORDER_NO,
    ORDER_ID,
    PRODUCT_CODE,
    ORDER_QTY,
    CMONEY
FROM
    EC_ORDER
WHERE
        PRODUCT_CODE = 'CM01'
    AND ORDER_QTY > 1
ORDER BY
    ORDER_ID;

-- 고씨 부터 최씨 제외 / 회원명 순 출력
SELECT
    NAME,
    REGIST_NO,
    TELEPHONE,
    ADDRESS
FROM
    EC_MEMBER
WHERE
    NAME NOT BETWEEN '고' AND '쵸'
ORDER BY
    NAME;

-- L1011 L1021 학점 취득한 학생 / 과목코드 학번순 출력
SELECT
    COURSE_ID,
    STUDENT_ID,
    SCORE,
    SCORE_ASSIGNED
FROM
    SG_SCORES
WHERE
    COURSE_ID IN ( 'L1011', 'L1021' )
    AND SCORE IS NOT NULL
    AND SCORE > 0
ORDER BY
    COURSE_ID,
    STUDENT_ID;

-- 성적이 80점 이상이 아닌 행
SELECT
    COURSE_ID,
    STUDENT_ID,
    SCORE,
    SCORE_ASSIGNED
FROM
    SG_SCORES
WHERE
    SCORE <= 80;

-- 컴공, 경영이 아닌 학생 / 학과 코드, 학번순 출력
