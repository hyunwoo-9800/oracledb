-- 삼성회사의 제품을 상품명순으로
SELECT
    PRODUCT_CODE,
    PRODUCT_NAME,
    UNIT,
    UNIT_PRICE,
    LEFT_QTY,
    COMPANY
FROM
    EC_PRODUCT
WHERE
    COMPANY = 'SAMSUNG'
ORDER BY
    PRODUCT_NAME;

-- 제품 단가 50만원 초과 단가 내림차순으로
SELECT
    PRODUCT_CODE,
    PRODUCT_NAME,
    UNIT,
    UNIT_PRICE,
    LEFT_QTY,
    COMPANY
FROM
    EC_PRODUCT
WHERE
    UNIT_PRICE > 500000
ORDER BY
    UNIT_PRICE DESC;

-- 정씨 회원정보 출력
SELECT
    NAME,
    REGIST_NO,
    TELEPHONE,
    TIMESTAMP
FROM
    EC_MEMBER
WHERE
    NAME LIKE '정%';

-- 현금, 계좌이체 정보 출려
SELECT
    ORDER_NO,
    PRODUCT_CODE,
    CSEL,
    CDATE
FROM
    EC_ORDER
WHERE
    CSEL IN ( '신용카드', '현금입금' )
ORDER BY
    CSEL,
    CDATE;

-- HP 회사의 프린터 제품 단가순 출력
SELECT
    PRODUCT_CODE,
    PRODUCT_NAME,
    UNIT,
    UNIT_PRICE,
    LEFT_QTY
FROM
    EC_PRODUCT
WHERE
        PRODUCT_NAME = '프린터'
    AND COMPANY = 'HP'
ORDER BY
    UNIT_PRICE;
 
 
-- 삼성과 LG가 아닌 제품 단가순 출력
SELECT
    PRODUCT_CODE,
    PRODUCT_NAME,
    UNIT,
    UNIT_PRICE,
    LEFT_QTY,
    COMPANY
FROM
    EC_PRODUCT
WHERE
    COMPANY NOT IN ( 'SAMSUNG', 'LG전자' )
ORDER BY
    COMPANY,
    UNIT_PRICE;    

-- 주소가 서울과 대구가 아닌 사람 주소순 출력
SELECT
    NAME,
    REGIST_NO,
    TELEPHONE,
    ADDRESS
FROM
    EC_MEMBER
WHERE
    ADDRESS NOT LIKE '서울%'
    AND ADDRESS NOT LIKE '대구%'
ORDER BY
    ADDRESS;

-- 재고수량 10미만 단가순 출력
SELECT
    PRODUCT_CODE,
    PRODUCT_NAME,
    STANDARD,
    UNIT_PRICE,
    LEFT_QTY,
    COMPANY
FROM
    EC_PRODUCT
WHERE
    LEFT_QTY < 10
ORDER BY
    PRODUCT_NAME; 

-- 전화번호 666이 포함 / 회원명 순 출력
